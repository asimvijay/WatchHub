  // Sorting function
  static List<Map<String, String>> sortWatches(List<Map<String, String>> watches, String sortBy) {
    switch (sortBy) {
      case 'priceLowToHigh':
        watches.sort((a, b) {
          double priceA = double.parse(a['price']!.replaceAll('\$', '').replaceAll(',', ''));
          double priceB = double.parse(b['price']!.replaceAll('\$', '').replaceAll(',', ''));
          return priceA.compareTo(priceB);
        });
        break;
      case 'priceHighToLow':
        watches.sort((a, b) {
          double priceA = double.parse(a['price']!.replaceAll('\$', '').replaceAll(',', ''));
          double priceB = double.parse(b['price']!.replaceAll('\$', '').replaceAll(',', ''));
          return priceB.compareTo(priceA);
        });
        break;
      case 'nameAtoZ':
        watches.sort((a, b) => a['title']!.compareTo(b['title']!));
        break;
      case 'nameZtoA':
        watches.sort((a, b) => b['title']!.compareTo(a['title']!));
        break;
      case 'brandAtoZ':
        watches.sort((a, b) => a['brand']!.compareTo(b['brand']!));
        break;
      case 'brandZtoA':
        watches.sort((a, b) => b['brand']!.compareTo(a['brand']!));
        break;
      default:
      // No sorting or default sorting
        break;
    }
    return watches;
  }

  // Filter watches based on search query
  static List<Map<String, String>> filterWatches(List<Map<String, String>> watches, String searchQuery) {
    if (searchQuery.isEmpty) {
      return watches; // Return all watches if no search query
    }
    return watches.where((watch) {
      final title = watch['title']!.toLowerCase();
      final brand = watch['brand']!.toLowerCase();
      final query = searchQuery.toLowerCase();
      return title.contains(query) || brand.contains(query);
    }).toList();
  }
